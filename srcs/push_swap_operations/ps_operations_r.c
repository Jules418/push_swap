#include "push_swap.h"

void	ra(t_ps p)
{
	if (!p)
		return ;
	rotate(p->a);
}

void	rb(t_ps p)
{
	if (!p)
		return ;
	rotate(p->b);
}

void	rr(t_ps p)
{
	ra(p);
	rb(p);
}
